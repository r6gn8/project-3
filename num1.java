public class num1 {
    public static void main(String[] args) {
        int n =1;
        int m = -1;
        if(n< -m)
        {System.out.println(n);}
        else{System.out.println(m);}

        if(-n>=m)
        {System.out.println(n);}
        else{System.out.println(m);}

        double x =0;
        double y =1;
        if(Math.abs(x-y)<1)
        {System.out.println(x);}
        else{System.out.println(y);}

        double c = Math.sqrt(2);
        double h=2;
        if(c*c==h)
        {System.out.println(c);}
        else{System.out.println(y);}
    }
}
